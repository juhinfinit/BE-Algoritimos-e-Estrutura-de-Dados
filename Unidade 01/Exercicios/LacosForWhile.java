public class LacosForWhile {

    public static void main(String[] args) {
        // Usando o laço for
        System.out.println("Usando for loop:");
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        // Usando o laço while
        System.out.println("\nUsando while loop:");
        int j = 1;
        while (j <= 10) {
            System.out.println(j);
            j++;
        }
    }
}
